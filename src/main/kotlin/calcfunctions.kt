fun main(){
  add(num1 = 20, num2 = 25)
  diff(num1 = 20, num2 = 25)
  multiply(num1 = 20, num2 = 25)
  divide(num1 = 20, num2 = 25)
}

fun add(num1:Int, num2:Int){
    println(num1+num2)
}

fun diff(num1:Int, num2:Int){
    println(num1-num2)
}

fun multiply(num1:Int, num2:Int){
    println(num1*num2)
}

fun divide(num1:Int, num2:Int){
    println(num1/num2)
}
